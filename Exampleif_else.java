class Exampleif_else
{
   public static void main(String[] args)
   {
   int money=100;
     if(money>=120)
     {
       System.out.println("You can buy ticket for a movie"); //If the condition become true it will return this line
     }
     else
     {
       System.out.println("You can not buy ticket for a movie"); //If the condition become false it will return this line
     }
   }
}

